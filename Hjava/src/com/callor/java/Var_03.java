package com.callor.java;

public class Var_03 {
	public static void main(String[] args) {
		
		int intNum1 =33;
		int intNum2 =77;
		
		int intSum = intNum1 + intNum2;
		int intProduct = intNum1 * intNum2;
		int intMinus = intNum1 - intNum2;
		int intDivision = intNum1 / intNum2;
		
		System.out.printf("\t%d + %d = %d\n", intNum1, intNum2, intSum);
		System.out.printf("\t%d * %d = %d\n", intNum1, intNum2, intProduct);
		System.out.printf("\t%d - %d = %d\n", intNum1, intNum2, intMinus);
		System.out.printf("\t%d / %d = %d\n", intNum1, intNum2, intDivision);
		
		
	}
}
